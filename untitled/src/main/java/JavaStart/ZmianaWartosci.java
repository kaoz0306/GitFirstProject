package JavaStart;

public class ZmianaWartosci {

        public static void main(String[] args) {
            // stworzenie zmiennej stanKonta o wartości 1000
            int stanKonta = 1000;
            // wypisanie stanu konta (przed zmianą)
            System.out.println("Stan konta przed zmianą: " + stanKonta);
            // zmiana wartości zmiennejStanKonta
            stanKonta = 1500;
            // wypisanie stanu konta (po zmianie)
            System.out.println("Stan konta po zmianie: " + stanKonta);
        }
    }

